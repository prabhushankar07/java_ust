public class RectangleCalculations {
    public static void main(String[] args) {
        double length = 10.5;
        double width = 5.2;

        double area = length * width;
        double perimeter = 2 * (length + width);

        System.out.println("Area: " + area);
        System.out.println("Perimeter: " + perimeter);
    }
}