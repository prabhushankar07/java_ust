public class arithmetic {
    public static void main(String[] args) {
        int a = 20;
        int b = 8;

        System.out.println("Sum: " + (a + b));
        System.out.println("Difference: " + (a - b));
        System.out.println("Product: " + (a * b));
        System.out.println("Quotient: " + ((double) a / b));
    }
}